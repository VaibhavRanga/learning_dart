void main(List<String> args) {
  //dynamic or var declaration without assignment can hold multiple data types in same variable
  var name;

  name = "Vaibhav";
  name = 17;
  name = 10.35;

  dynamic data = 100;
  data = 12235.454;
  data = "Dinesh";

  //var can hold data without declaring any data type
  var name1 = "Karuna";
  name1 = "Dinesh";
  //data type of the first assignment can be assigned afterwards
}
